class GrayscaleToRGB(Transform):
    def __init__(
        self,
    ) -> None:
        pass

    def __call__(self, img: np.ndarray) -> np.ndarray:

        # squeeze img
        img = img.squeeze()

        # ensure that img is 2D
        if not np.ndim(img) == 2:
            raise ValueError("Image dimensions must be equal to 2")

        # repeat grayscale image along first dimension (color channel)
        img = np.tile(img, (3,1,1))

        return img

class GrayscaleToRGBd(MapTransform):
    def __init__(
        self,
        keys: KeysCollection,
        allow_missing_keys: bool = False,
    ) -> None:

        super().__init__(keys, allow_missing_keys)
        self.grayscale_to_rgb = GrayscaleToRGB()

    def __call__(
        self, data: Mapping[Hashable, np.ndarray]
    ) -> Dict[Hashable, np.ndarray]:
        d = dict(data)
        for key in self.key_iterator(d):
            d[key] = self.grayscale_to_rgb(d[key])
        return d

class Squeeze2Dd(MapTransform):
    def __init__(
        self,
        keys: KeysCollection,
        allow_missing_keys: bool = False,
    ) -> None:

        super().__init__(keys, allow_missing_keys)

    def __call__(
        self, data: Mapping[Hashable, np.ndarray]
    ) -> Dict[Hashable, np.ndarray]:
        d = dict(data)
        for key in self.key_iterator(d):
            # first squeeze
            img = (d[key]).squeeze()

            # ensure that img is 2D
            if not np.ndim(img) == 2:
                raise ValueError("Image dimensions must be equal to 2")

            # reshape to (1, img.shape[0], img.shape[1])
            img = img.reshape((1, *img.shape))

            d[key] = img
        return d

# custom dataset class
class BraTSDataset(PersistentDataset):
    
    def __init__(self, dataset_dir, cache_dir):

        # paths
        self.dataset_dir = dataset_dir
        self.cache_dir = cache_dir
        
        # BraTS tables
        self.brats_df = self.get_brats_df() # first get main df
        self.brats_df = self.get_slices_df() # then update df to include paths to slices

        # MONAI sequence of transforms
        self.slice_idx = 75
        self.transform_seq = Compose([
            LoadImaged(keys = ["img"]),
            # SpatialCropd(keys = ["img"], roi_slices = [slice(None), slice(self.slice_idx, self.slice_idx+1), slice(None)]),
            Squeeze2Dd(keys = ["img"]),
            Resized(keys = ["img"], spatial_size = (128, 128), size_mode = "all"),  # resize to 1 x 128 x 128
            GrayscaleToRGBd(keys = ["img"]), # make into 3 RGB channels
            RandFlipd(keys = ["img"], prob = 0.5, spatial_axis = 0), # random left-right axis flip
            ToTensord(keys = ["img"]),
        ])

        super().__init__(
            data = [{"img": nii_path, "index": i} for i, nii_path in enumerate(self.brats_df["slice_path"])],
            transform = self.transform_seq,
            cache_dir = self.cache_dir,
        )

    def get_brats_df(self):

        # load CSVs
        name_mapping_df = pd.read_csv(os.path.join(self.dataset_dir, "name_mapping.csv"))
        survival_df = pd.read_csv(os.path.join(self.dataset_dir, "survival_info.csv"))
        survival_df = survival_df.rename({"Brats20ID": "BraTS_2020_subject_ID"}, axis = 1) # rename column for merge

        # merge
        brats_df = name_mapping_df.merge(survival_df, how = "outer", on = "BraTS_2020_subject_ID")

        # get subject-specific paths for all image modalities
        def get_subj_nii_path(subj, modality, wdir):
            return os.path.join(wdir, subj, subj + "_" + modality + ".nii")
        for modality in ["t1", "t2", "flair", "t1ce", "seg"]:
            brats_df[modality + "_path"] = brats_df["BraTS_2020_subject_ID"].map(
                lambda subj: get_subj_nii_path(subj, modality, self.dataset_dir)
            )

        return brats_df

    def get_slices_df(self):

        def get_slice_df_subj(subj, dataset_dir, modality):
            # define wdir path
            wdir = os.path.join(dataset_dir, subj)

            # load segmentation stats dataframe
            seg_stats_df = pd.read_csv(os.path.join(wdir, f"{subj}_seg_stats.txt"), delim_whitespace=True)
            slices_with_tumor = seg_stats_df[seg_stats_df["max"] > 0]["slice_num"].values

            # get paths to slices with tumor and create dataframe
            slices_with_tumor_str = np.char.zfill(slices_with_tumor.astype(str), 4)
            slice_path = [os.path.join(dataset_dir, subj, f"{modality}_slices", f"{subj}_{modality}_slice_{s}.nii.gz") for s in slices_with_tumor_str]
            slice_path_df = pd.DataFrame({"slice_path": slice_path})
            slice_path_df["BraTS_2020_subject_ID"] = subj

            return slice_path_df
        
        brats_slice_df = pd.concat([get_slice_df_subj(subj, self.dataset_dir, "t1") for subj in self.brats_df["BraTS_2020_subject_ID"]])
        brats_slice_df = pd.merge(
            self.brats_df,
            brats_slice_df,
            how = "outer",
            on = "BraTS_2020_subject_ID"
        )

        return brats_slice_df